const Services = ({ data }) => {
  const sectionRef = useRef(null);
  const cardsRef = useRef([]);

  useEffect(() => {
    const observer = new IntersectionObserver(
      (entries) => {
        entries.forEach((entry) => {
          if (entry.isIntersecting) {
            entry.target.classList.add('animate-on-load');
            observer.unobserve(entry.target);
          }
        });
      },
      { threshold: 0.1 }
    );

    if (sectionRef.current) {
      observer.observe(sectionRef.current);
    }

    return () => observer.disconnect();
  }, []);

  useEffect(() => {
    const observer = new IntersectionObserver(
      (entries) => {
        entries.forEach((entry) => {
          if (entry.isIntersecting) {
            entry.target.classList.add('card-reveal');
            observer.unobserve(entry.target);
          }
        });
      },
      { threshold: 0.15 }
    );

    cardsRef.current.forEach((card) => {
      if (card) observer.observe(card);
    });

    return () => observer.disconnect();
  }, [data.items]);

  return (
    <section id="services" ref={sectionRef} style={{ opacity: 0 }}>
      <h2 className="section-title">{data.title}</h2>
      <div className="services-grid">
        {data.items.map((item, i) => (
          <div
            key={item.id}
            className="service-card card-hidden"
            ref={(el) => (cardsRef.current[i] = el)}
            style={{ transitionDelay: `${i * 120}ms` }}
          >
            <div className="service-icon-placeholder">
              {getIcon(item.id)}
            </div>
            <h3>{item.title}</h3>
            <p>{item.description}</p>
            {item.features && (
              <ul className="service-features">
                {item.features.map((f, idx) => (
                  <li key={idx}>{f}</li>
                ))}
              </ul>
            )}
          </div>
        ))}
      </div>
    </section>
  );
};

export default Services;
